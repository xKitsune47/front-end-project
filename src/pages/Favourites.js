import Header from "../components/Header";
import { useForecast } from "../contexts/ForecastContext";

function Favourites({ children }) {
    document.title = "Ulubione";

    const { saved, handleCity } = useForecast();

    return (
        <div>
            <Header />
            <div className="favs-container">
                <table key="favs-table">
                    <tbody key="favs-table-body">
                        {saved?.map((city, i) => {
                            return (
                                <>
                                    <tr key={city} className="fav-city">
                                        <td>
                                            <DeleteCity
                                                onClick={() => handleCity(city)}
                                                city={city}
                                                key={city}
                                            />
                                        </td>
                                        <td>{city}</td>
                                    </tr>
                                    {!saved[i + 1] ? (
                                        ""
                                    ) : (
                                        <tr
                                            className="separator"
                                            key={`${city}-separator`}>
                                            <td colSpan={2}>
                                                <hr />
                                            </td>
                                        </tr>
                                    )}
                                </>
                            );
                        })}
                    </tbody>
                </table>
            </div>
        </div>
    );
}

function DeleteCity({ children, onClick, city }) {
    return (
        <svg
            onClick={() => onClick(city)}
            xmlns="http://www.w3.org/2000/svg"
            width="16"
            height="16"
            fill="currentColor"
            className="bi bi-trash3-fill"
            viewBox="0 0 16 16">
            <path d="M11 1.5v1h3.5a.5.5 0 0 1 0 1h-.538l-.853 10.66A2 2 0 0 1 11.115 16h-6.23a2 2 0 0 1-1.994-1.84L2.038 3.5H1.5a.5.5 0 0 1 0-1H5v-1A1.5 1.5 0 0 1 6.5 0h3A1.5 1.5 0 0 1 11 1.5m-5 0v1h4v-1a.5.5 0 0 0-.5-.5h-3a.5.5 0 0 0-.5.5M4.5 5.029l.5 8.5a.5.5 0 1 0 .998-.06l-.5-8.5a.5.5 0 1 0-.998.06m6.53-.528a.5.5 0 0 0-.528.47l-.5 8.5a.5.5 0 0 0 .998.058l.5-8.5a.5.5 0 0 0-.47-.528M8 4.5a.5.5 0 0 0-.5.5v8.5a.5.5 0 0 0 1 0V5a.5.5 0 0 0-.5-.5" />
        </svg>
    );
}

export default Favourites;

